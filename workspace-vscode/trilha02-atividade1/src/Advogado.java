public class Advogado {

    private String oab;
    private String nome;

    Advogado(String oab, String nome) {
        this.oab = oab;
        this.nome = nome;
    }

    public String getOab() {
        return oab;
    }

    public void setOab(String oab) {
        this.oab = oab;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }



}
